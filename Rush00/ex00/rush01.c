int	ft_putchar(char a, char b, char c, int x);

int	rush(int x, int y)
{
	int	i;

	i = 1;
	ft_putchar('/', '*', '\\', x);
	while (i <= (y - 2))
	{
		ft_putchar('*', ' ', '*', x);
		i = i + 1;
	}
	if (y >= 2)
	{
		ft_putchar('\\', '*', '/', x);
	}
	return (0);
}
